package cn.richinfo.pojo;

public class WinResult {
	
	private String code;
	
	private String title;
	
	private String kind;
	
	private String projectCode;
	
	private String mailTemp;
	
	private String picName;
	
	private String magazineIds;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

	public String getProjectCode() {
		return projectCode;
	}

	public void setProjectCode(String projectCode) {
		this.projectCode = projectCode;
	}

	public String getMailTemp() {
		return mailTemp;
	}

	public void setMailTemp(String mailTemp) {
		this.mailTemp = mailTemp;
	}

	public String getPicName() {
		return picName;
	}

	public void setPicName(String picName) {
		this.picName = picName;
	}

	public String getMagazineIds() {
		return magazineIds;
	}

	public void setMagazineIds(String magazineIds) {
		this.magazineIds = magazineIds;
	}
	
	

}
